package ex3;


public interface Ex3IntSequence {
	
	/**
	 * This represents the first int in a sequence.
	 * @return int - first int
	 */
	public int firstInt();
	
	/**
	 * This represents the next int in a sequence of numbers (based off the last int).
	 * @return int - next int
	 */
	public int nextInt();

}
